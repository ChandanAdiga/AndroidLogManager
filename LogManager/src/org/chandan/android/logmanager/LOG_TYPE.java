package org.chandan.android.logmanager;

/**
 * Available category of log messages.
 * @author chandan
 */
public enum LOG_TYPE {
	/**
	 * Log type corresponding to information.
	 */
	INFO,
	
	/**
	 * Log type corresponding to debugging.
	 */
	DEBUG,
	
	/**
	 * Log type corresponding to warning.
	 */
	WARNING,
	
	/**
	 * Log type corresponding to error.
	 */
	ERROR
}
